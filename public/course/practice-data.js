const slider = (variable, key, label, min, max, step, value) => ({
  variable,
  key,
  label,
  min,
  max,
  step,
  value,
});

const color = (variable, label, value) => ({
  variable,
  key: "accent",
  label,
  type: "color",
  value,
});

function practice({
  preview,
  title,
  intro,
  action,
  filename = "decision.css",
  kind = "lesson experiment",
  copy,
  controls,
}) {
  const declarations = controls
    .map((control) => `  --${control.variable}: ${control.type === "color" ? control.value : Number(control.value).toFixed(2)};`)
    .join("\n");

  return {
    preview,
    actionPrompt: action,
    previewCopy: copy,
    controls,
    lab: {
      filename,
      kind,
      title,
      help: "Change the decision, run the frame, and inspect the consequence.",
      intro,
      ariaLabel: `Editable ${filename} experiment`,
      code: `:root {\n${declarations}\n}\n\n/* One decision. One visible consequence. */`,
    },
  };
}

